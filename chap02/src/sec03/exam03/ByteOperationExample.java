package sec03.exam03;

public class ByteOperationExample {

	public static void main(String[] args) {
		//int 에서 byte로의 casting 형변환
		byte result1 = 10 + 20;
		System.out.println(result1);

		//byte에서 int로의 promotion 형변환
		byte x = 10;
		byte y = 20;
		int result2 = x + y;
		System.out.println(result2);
		
	}

}
